#include "./HCSR04.h"

/* *************************************************************************************
Constructors
************************************************************************************* */
HCSR04Interrupt::HCSR04Interrupt(HCSR04* ownerptr, uint32_t pin)
{
	InterruptOwnerPtr = ownerptr;
	_pin = pin;

	Register(_pin, this, CHANGE);
}

HCSR04Interrupt::~HCSR04Interrupt()
{
	Deregister(this);
}

/* *************************************************************************************
public Functions
************************************************************************************* */
void HCSR04Interrupt::HandledISR(void)
{
	InterruptOwnerPtr->PinChange(micros());
}

